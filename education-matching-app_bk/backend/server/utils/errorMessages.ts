/**
 * Get translated error message
 * @param messageKey - Error message key constant or string (for backward compatibility)
 * @returns Japanese translation
 */
export function getErrorMessage(messageKey: ErrorMessageKey | string): string {
  // If it's already a key constant, use it directly
  if (messageKey in ERROR_MESSAGES) {
    return ERROR_MESSAGES[messageKey as ErrorMessageKey];
  }
  
  // Otherwise, try to map from old English string
  const key = STRING_TO_KEY_MAP[messageKey];
  if (key) {
    return ERROR_MESSAGES[key];
  }
  
  // Fallback: return the original message
  return messageKey;
}

/**
 * Get dynamic error message with replacements
 * Handles messages with dynamic parts
 */
export function getErrorMessageWithParams(messageKey: ErrorMessageKey, params: Record<string, string>): string {
  // Handle dynamic messages
  if (messageKey === ERROR_MESSAGE_KEYS.FAILED_TO_REGISTER_USER) {
    return params.userType === "teacher" ? "先生の登録に失敗しました" : "生徒の登録に失敗しました";
  }
  
  if (messageKey === ERROR_MESSAGE_KEYS.USER_TYPE_NOT_FOUND) {
    if (params.userType === "teacher") return "先生が見つかりません";
    if (params.userType === "student") return "生徒が見つかりません";
    return "ユーザーが見つかりません";
  }
  
  // Fallback to simple translation
  return getErrorMessage(messageKey);
}
